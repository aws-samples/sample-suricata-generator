    def _on_region_change(self, *args) -> None:
        """Handle region dropdown change — refresh the model list."""
        self._refresh_models()

    def _refresh_models(self) -> None:
        """Query available models via AgentFactory.list_models() in background thread."""
        region = self._region_var.get()
        if self._model_combo is not None:
            self._model_var.set("Loading models\u2026")
            self._model_combo.config(state="disabled")

        def _query():
            try:
                models = AgentFactory.list_models(self._app.aws_session, region)
            except Exception as exc:
                logger.warning("Failed to list models in %s: %s", region, exc)
                models = []
            # Marshal back to the main thread
            try:
                self._app.root.after(0, lambda: self._populate_models(models))
            except tk.TclError:
                pass  # Root window destroyed

        thread = threading.Thread(target=_query, daemon=True)
        thread.start()

    def _populate_models(self, models: list[dict]) -> None:
        """Populate model dropdown with same default selection priority as AI Rule Assistant.

        Priority order: (1) Claude Opus 4, (2) Claude Sonnet 4,
        (3) any other Claude model, (4) first available model.
        Excludes deprecated versions (e.g., opus-4-1 when selecting opus-4).
        """
        # Guard against destroyed window
        if self._tab_frame is None:
            return
        try:
            if not self._tab_frame.winfo_exists():
                return
        except tk.TclError:
            return

        self._model_list = models

        if models:
            display_names = [
                m.get("modelName", m.get("modelId", "unknown")) for m in models
            ]
            self._model_combo["values"] = display_names
            self._model_combo.config(state="readonly")

            # Try to select the best Claude model by default.
            # Preference order (highest first):
            #   1. Claude Opus 4  (claude-opus-4, not opus-4-1)
            #   2. Claude Sonnet 4
            #   3. Any other Claude model
            selected_idx = 0
            best_priority = -1  # -1 = no Claude found yet
            for i, m in enumerate(models):
                mid = m.get("modelId", "").lower()
                mname = m.get("modelName", "").lower()
                combined = mid + " " + mname

                if "claude" not in combined:
                    continue

                # Determine priority for this model
                if "opus-4" in combined and "opus-4-1" not in combined:
                    priority = 3  # Claude Opus 4 (latest flagship)
                elif "sonnet-4" in combined and "sonnet-4-1" not in combined:
                    priority = 2  # Claude Sonnet 4
                else:
                    priority = 1  # Any other Claude model

                if priority > best_priority:
                    best_priority = priority
                    selected_idx = i

            self._model_var.set(display_names[selected_idx])
        else:
            self._model_combo["values"] = ["(no models found)"]
            self._model_combo.config(state="readonly")
            self._model_var.set("(no models found)")

    # ------------------------------------------------------------------ #
    #  Analysis Trigger / Background Execution                            #
    # ------------------------------------------------------------------ #

    def _trigger_analysis(self) -> None:
        """Handle AI Deep Analysis button click. Runs in background thread."""
        # Reset cancellation flag
        self._cancel_flag = False

        # --- Resolve model ID from display name ---
        model_display = self._model_var.get() if self._model_var else ""
        model_id = ""
        for m in self._model_list:
            name = m.get("modelName", m.get("modelId", ""))
            if name == model_display:
                model_id = m.get("modelId", "")
                break
        if not model_id:
            self._show_error("No model selected. Please select a model and try again.")
            return

        region = self._region_var.get() if self._region_var else "us-east-1"

        # --- Disable controls, show cancel button and progress ---
        if self._analyze_btn is not None:
            self._analyze_btn.config(state=tk.DISABLED)
        if self._cancel_btn is not None:
            self._cancel_btn.pack(side=tk.LEFT, padx=(4, 0))
        if self._status_label is not None:
            self._status_label.config(text="Preparing analysis\u2026")

        # Show progress message in content area
        if self._content_area is not None:
            self._content_area.config(state=tk.NORMAL)
            self._content_area.delete("1.0", tk.END)
            self._content_area.insert("1.0", "\n\u23f3 Analyzing ruleset\u2026\n\n")
            self._content_area.tag_add("center", "1.0", tk.END)
            self._content_area.tag_configure("center", justify=tk.CENTER)
            self._content_area.config(state=tk.DISABLED)

        # Track progress stages and elapsed time for the animated indicator
        import time
        self._analysis_start_time = time.time()
        self._progress_stages: list[str] = []
        self._elapsed_timer_id = None
        self._last_quip_elapsed = 0  # track when we last showed a quip

        # Humorous waiting messages shown every ~10 seconds
