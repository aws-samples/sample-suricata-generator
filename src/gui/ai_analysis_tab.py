    def _on_close(self) -> None:
        """Close the report window containing this tab."""
        try:
            toplevel = self._tab_frame.winfo_toplevel()
            if toplevel.winfo_exists():
                toplevel.destroy()
        except tk.TclError:
            pass

    def _copy_selection(self, event=None) -> str:
        """Copy selected text from the content area to clipboard."""
        try:
            sel = self._content_area.get(tk.SEL_FIRST, tk.SEL_LAST)
            self._app.root.clipboard_clear()
            self._app.root.clipboard_append(sel)
        except tk.TclError:
            pass
        return "break"

    def _on_mousewheel(self, event) -> None:
        """Handle cross-platform mouse wheel scrolling."""
        if self._content_area is None:
            return
        if event.num == 4:  # macOS scroll up
            self._content_area.yview_scroll(-3, "units")
        elif event.num == 5:  # macOS scroll down
            self._content_area.yview_scroll(3, "units")
        else:  # Windows/Linux
            self._content_area.yview_scroll(int(-1 * (event.delta / 120)), "units")
