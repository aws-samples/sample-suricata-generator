    def _save_html(self) -> None:
        """Export AI analysis results as a self-contained HTML file.

        Opens a file-save dialog, generates the HTML via the module-level
        ``generate_html_report()`` function, and writes it to disk.
        """
        if self._results is None:
            return

        filename = filedialog.asksaveasfilename(
            title="Save AI Analysis Report as HTML",
            defaultextension=".html",
            filetypes=[("HTML files", "*.html"), ("All files", "*.*")],
        )
        if not filename:
            return

        try:
            html_content = generate_html_report(self._results)
            with open(filename, "w", encoding="utf-8") as f:
                f.write(html_content)
            messagebox.showinfo("Success", f"Report saved as {filename}")
        except Exception as exc:
            messagebox.showerror("Error", f"Failed to save HTML report: {exc}")

    def _save_pdf(self) -> None:
        """Export AI analysis results as PDF via browser print.

        Generates a temporary HTML file using ``generate_html_report()``
        and opens it in the default browser so the user can use the
        browser's "Print > Save as PDF" function.  This follows the same
        approach as the existing ``save_report_pdf()`` in
        ``suricata_generator.py``.
        """
        if self._results is None:
            return

        try:
            html_content = generate_html_report(self._results)

            with tempfile.NamedTemporaryFile(
                mode="w", suffix=".html", delete=False, encoding="utf-8",
            ) as tmp:
                tmp.write(html_content)
                tmp.flush()
                temp_path = tmp.name

            webbrowser.open("file://" + os.path.abspath(temp_path))
            messagebox.showinfo(
                "PDF Export",
                "Report opened in browser. Use browser's Print > Save as PDF "
                "function to create PDF.",
            )
        except Exception as exc:
            messagebox.showerror("Error", f"Failed to export PDF: {exc}")

