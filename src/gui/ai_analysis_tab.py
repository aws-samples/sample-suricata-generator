    def _render_finding(
        self,
        text: tk.Text,
        finding: AnalysisFinding,
        colors: dict,
    ) -> None:
        """Render a single finding with checkbox, severity icon, and details."""
        # ── Checkbox (embedded widget) ────────────────────────────────
        var = tk.BooleanVar(value=False)
        self._finding_checkboxes[finding.id] = var

        cb = tk.Checkbutton(
            text,
            variable=var,
            command=self._update_send_button,
            bg=text.cget("bg"),
            activebackground=text.cget("bg"),
            highlightthickness=0,
            bd=0,
        )
        text.window_create(tk.END, window=cb)

        # ── Severity icon + label ─────────────────────────────────────
        sev = finding.severity.lower()
        icon = self._SEVERITY_ICONS.get(sev, "\u2139\ufe0f")
        sev_label = sev.upper()
        sev_tag = sev if sev in ("critical", "warning", "info") else "info"
        text.insert(tk.END, f" {icon} {sev_label} ", sev_tag)

        # ── Confidence badge ──────────────────────────────────────────
        conf = finding.confidence.lower()
        conf_tag = f"confidence_{conf}" if conf in ("high", "medium", "low") else "confidence_low"
        text.insert(tk.END, f"[confidence: {conf}]", conf_tag)
        text.insert(tk.END, "\n")

        # ── Description ──────────────────────────────────────────────
        text.insert(tk.END, f"  {finding.description}\n", "description")

        # ── Affected rules ────────────────────────────────────────────
        if finding.affected_sids:
            sids_str = ", ".join(f"SID {s}" for s in finding.affected_sids)
            text.insert(tk.END, f"  Affected rules: {sids_str}\n", "detail")

        # ── Recommendation ────────────────────────────────────────────
        if finding.recommendation:
            text.insert(tk.END, f"  Recommendation: {finding.recommendation}\n", "detail")

        # ── Caveat ────────────────────────────────────────────────────
        if finding.caveat:
            text.insert(tk.END, f"  \u2139\ufe0f {finding.caveat}\n", "caveat")

        # ── Reference ─────────────────────────────────────────────────
        if finding.reference:
            text.insert(tk.END, f"  Ref: {finding.reference}\n", "reference")

        text.insert(tk.END, "\n")

    def _get_filtered_sorted_findings(
        self,
        response: StructuredAnalysisResponse,
    ) -> dict[str, list[AnalysisFinding]]:
        """Return findings organized by section, applying current sort order."""
        sort_key = self._sort_var.get() if self._sort_var else "Severity"

        result: dict[str, list[AnalysisFinding]] = {}
        for section_key, _title, _prefix in self._SECTIONS:
            findings = list(getattr(response, section_key, []))
            if sort_key == "Severity":
                findings.sort(key=lambda f: self._SEVERITY_ORDER.get(f.severity.lower(), 9))
            result[section_key] = findings
        return result

    def _build_sort_filter_bar(self, response: StructuredAnalysisResponse) -> None:
        """Build or rebuild the sort/filter/select-all bar below the content area.

        This bar is packed between the content area and the button frame.
        """
        # Remove existing bar if present
        bar_attr = "_sort_filter_bar"
        existing = getattr(self, bar_attr, None)
        if existing is not None:
            try:
                existing.destroy()
            except tk.TclError:
                pass

        bar = ttk.Frame(self._tab_frame)
        bar.pack(side=tk.BOTTOM, fill=tk.X, padx=10, pady=(0, 5))
        setattr(self, bar_attr, bar)

        # Sort dropdown
        ttk.Label(bar, text="Sort:").pack(side=tk.LEFT, padx=(0, 4))
        if self._sort_var is None:
            self._sort_var = tk.StringVar(value="Severity")
        sort_combo = ttk.Combobox(
            bar,
            textvariable=self._sort_var,
            values=["Severity"],
            state="readonly",
            width=12,
        )
        sort_combo.pack(side=tk.LEFT, padx=(0, 12))
        sort_combo.bind("<<ComboboxSelected>>",
                        lambda _e: self._display_results(response))

        # Filter dropdown
        ttk.Label(bar, text="Filter:").pack(side=tk.LEFT, padx=(0, 4))
        filter_values = ["All findings"]
        for section_key, section_title, _prefix in self._SECTIONS:
            if getattr(response, section_key, []):
                filter_values.append(section_title)

        if self._filter_var is None:
            self._filter_var = tk.StringVar(value="All findings")
        filter_combo = ttk.Combobox(
            bar,
            textvariable=self._filter_var,
            values=filter_values,
            state="readonly",
            width=22,
        )
        filter_combo.pack(side=tk.LEFT, padx=(0, 12))
        filter_combo.bind("<<ComboboxSelected>>",
                          lambda _e: self._display_results(response))

        # Select All checkbox
        if self._select_all_var is None:
            self._select_all_var = tk.BooleanVar(value=False)
        else:
            self._select_all_var.set(False)

        select_all_cb = ttk.Checkbutton(
            bar,
            text="Select All",
            variable=self._select_all_var,
            command=lambda: self._toggle_select_all(),
        )
        select_all_cb.pack(side=tk.LEFT, padx=(0, 4))

    def _toggle_select_all(self) -> None:
        """Toggle all finding checkboxes based on the Select All state."""
        select_all = self._select_all_var.get() if self._select_all_var else False
        for var in self._finding_checkboxes.values():
            var.set(select_all)
        self._update_send_button()

    def _update_send_button(self) -> None:
        """Update the 'Send to AI Rule Assistant' button text and state.

        Enabled when >= 1 finding is selected; shows count in parentheses.
        """
        selected_count = sum(
            1 for v in self._finding_checkboxes.values() if v.get()
        )
        if self._send_btn is not None:
            try:
                if selected_count > 0:
                    self._send_btn.config(
                        text=f"Send to AI Rule Assistant ({selected_count})",
                        state=tk.NORMAL,
                    )
                else:
                    self._send_btn.config(
                        text="Send to AI Rule Assistant",
                        state=tk.DISABLED,
                    )
            except tk.TclError:
                pass

    # ------------------------------------------------------------------ #
    #  Finding Handoff                                                    #
    # ------------------------------------------------------------------ #

    def _send_to_assistant(self) -> None:
        """Handoff selected findings to AI Rule Assistant.

        Collects the user-selected findings, builds a focused handoff prompt
        via ``AIRuleAnalyzer.build_handoff_prompt()``, opens the AI Rule
        Assistant panel (which triggers the disclaimer if not yet accepted),
        and inserts the prompt into the assistant's input field without
        auto-submitting.

        If the user declines the disclaimer, the handoff is cancelled and
        finding selections are preserved.
        """
        # ── Guard: need results and an analyzer instance ──────────────
        if self._results is None or self._ai_analyzer is None:
            return

        # ── Collect selected findings ─────────────────────────────────
        selected_findings: list[AnalysisFinding] = []
        all_findings = self._results.all_findings
        findings_by_id = {f.id: f for f in all_findings}

        for finding_id, var in self._finding_checkboxes.items():
            if var.get() and finding_id in findings_by_id:
                selected_findings.append(findings_by_id[finding_id])

        if not selected_findings:
            return  # Nothing selected — button should be disabled anyway

        # ── Build the handoff prompt ──────────────────────────────────
        rules = list(self._app.rules) if hasattr(self._app, "rules") else []
        handoff_prompt = self._ai_analyzer.build_handoff_prompt(
            selected_findings, rules,
        )

        # ── Open the AI Rule Assistant panel ──────────────────────────
        # show() triggers the disclaimer dialog if not yet accepted.
        # Lazily create the panel if it hasn't been opened yet (mirrors
        # the pattern in SuricataRuleGenerator.show_ai_assistant).
        if self._app.ai_panel is None:
            from src.gui.ai_assistant_panel import AIAssistantPanel
            self._app.ai_panel = AIAssistantPanel(self._app)
        ai_panel = self._app.ai_panel
        ai_panel.show()

        # ── Check if the disclaimer was accepted ──────────────────────
        # If the user declined, _disclaimer_accepted remains False and
        # the panel window is not created. Preserve finding selections.
        if not ai_panel._disclaimer_accepted:
            return

        # ── Set placement context for positional insertion ────────────
        # Find the highest affected SID across all selected findings so
        # that generated rules are inserted right after it instead of at
        # the end of the file.
        all_affected_sids: list[int] = []
        for f in selected_findings:
            all_affected_sids.extend(f.affected_sids)
        if all_affected_sids:
            ai_panel._pending_insert_after_sid = max(all_affected_sids)
        else:
            ai_panel._pending_insert_after_sid = None

        # ── Insert the handoff prompt into the input field ────────────
        # Do NOT auto-submit — let the user review, edit, and submit.
        if ai_panel.input_field is not None:
            try:
                ai_panel.input_field.delete("1.0", tk.END)
                ai_panel.input_field.insert("1.0", handoff_prompt)
                ai_panel.input_field.focus_set()
            except tk.TclError:
                pass

