def generate_html_report(response: "StructuredAnalysisResponse") -> str:
    """Generate a self-contained HTML report from an AI analysis response.

    The returned HTML includes inline CSS, is organized by section, and
    contains severity icons, confidence badges, the AI disclaimer, and a
    timestamp.  It has no external dependencies so it can be saved as a
    single ``.html`` file or opened directly in a browser for PDF printing.

    This is a module-level function so it can be imported and tested
    independently of the ``AIAnalysisTab`` UI class.
    """
    from html import escape

    # ── Timestamp formatting ──────────────────────────────────────────
    timestamp_display = response.timestamp or "unknown"
    try:
        from datetime import datetime
        dt = datetime.fromisoformat(timestamp_display.replace("Z", "+00:00"))
        timestamp_display = dt.strftime("%B %d, %Y %H:%M:%S")
    except Exception:
        pass

    model_display = escape(response.model_id) if response.model_id else "unknown"

    # ── Inline CSS ────────────────────────────────────────────────────
    css = """\
    body {
        font-family: -apple-system, BlinkMacSystemFont, "Segoe UI", Roboto,
                     "Helvetica Neue", Arial, sans-serif;
        max-width: 900px;
        margin: 0 auto;
        padding: 24px;
        color: #212121;
        background: #fff;
        line-height: 1.5;
    }
    h1 { font-size: 1.6em; margin-bottom: 4px; }
    .meta { color: #757575; font-size: 0.85em; margin-bottom: 16px; }
    .disclaimer {
        background: #FFF3E0;
        border: 1px solid #FFB74D;
        border-radius: 6px;
        padding: 12px 16px;
        margin-bottom: 20px;
        color: #E65100;
        font-size: 0.9em;
    }
    .section-header {
        font-size: 1.15em;
        font-weight: 600;
        margin-top: 24px;
        margin-bottom: 4px;
        color: #37474F;
    }
    .section-sep {
        border: none;
        border-top: 1px solid #B0BEC5;
        margin: 4px 0 12px 0;
    }
    .finding {
        background: #FAFAFA;
        border: 1px solid #E0E0E0;
        border-radius: 6px;
        padding: 12px 16px;
        margin-bottom: 12px;
    }
    .finding-header { font-weight: 600; margin-bottom: 4px; }
    .severity-critical { color: #D32F2F; }
    .severity-warning  { color: #E65100; }
    .severity-info     { color: #1565C0; }
    .confidence {
        display: inline-block;
        font-size: 0.8em;
        padding: 1px 8px;
        border-radius: 10px;
        margin-left: 6px;
        vertical-align: middle;
    }
    .confidence-high   { background: #E8F5E9; color: #2E7D32; }
    .confidence-medium { background: #FFF8E1; color: #F57F17; }
    .confidence-low    { background: #F5F5F5; color: #757575; }
    .description { margin: 6px 0; }
    .detail { color: #616161; font-size: 0.9em; margin: 2px 0; }
    .caveat { color: #78909C; font-size: 0.88em; font-style: italic; margin: 4px 0; }
    .reference { color: #00695C; font-size: 0.88em; margin: 2px 0; }
    .notice {
        background: #E3F2FD;
        border: 1px solid #90CAF9;
        border-radius: 6px;
        padding: 10px 14px;
        margin-bottom: 16px;
        color: #1565C0;
        font-size: 0.9em;
    }
    .footer {
        margin-top: 32px;
        padding-top: 12px;
        border-top: 1px solid #E0E0E0;
        color: #9E9E9E;
        font-size: 0.8em;
        text-align: center;
    }
    @media print {
        body { max-width: 100%; padding: 12px; }
        .finding { break-inside: avoid; }
    }
    """

    # ── Build HTML body ───────────────────────────────────────────────
    parts: list[str] = []
    parts.append("<!DOCTYPE html>")
    parts.append('<html lang="en">')
    parts.append("<head>")
    parts.append('<meta charset="utf-8">')
    parts.append("<title>AI Rule Analysis Report</title>")
    parts.append(f"<style>{css}</style>")
    parts.append("</head>")
    parts.append("<body>")

    # Title and metadata
    parts.append("<h1>AI Rule Analysis Report</h1>")
    finding_count = response.finding_count
    cache_label = "Cached results" if response.is_cached else "Fresh analysis"
    parts.append(
        f'<p class="meta">{escape(cache_label)} &middot; '
        f'{finding_count} finding{"s" if finding_count != 1 else ""} &middot; '
        f'{escape(timestamp_display)}<br>'
        f'Model: {model_display}</p>'
    )

    # Disclaimer
    parts.append(
        f'<div class="disclaimer">\u26a0\ufe0f {escape(_DISCLAIMER_TEXT)}</div>'
    )

    # Summarization notice
    if response.was_summarized:
        parts.append(
            '<div class="notice">\u2139\ufe0f Analysis was performed on a '
            "prioritized subset of rules. Rules with static analysis findings "
            "were prioritized.</div>"
        )

    # Sections
    any_findings = False
    for section_key, section_title, _prefix in _SECTION_META:
        findings = getattr(response, section_key, [])
        if not findings:
            continue
        any_findings = True

        parts.append(
            f'<div class="section-header">{escape(section_title)} '
            f'({len(findings)} finding{"s" if len(findings) != 1 else ""})</div>'
        )
        parts.append('<hr class="section-sep">')

        for finding in findings:
            sev = finding.severity.lower()
            icon = _SEVERITY_HTML_ICONS.get(sev, "\u2139\ufe0f")
            sev_class = f"severity-{sev}" if sev in ("critical", "warning", "info") else "severity-info"

            conf = finding.confidence.lower()
            conf_class = f"confidence-{conf}" if conf in ("high", "medium", "low") else "confidence-low"

            parts.append('<div class="finding">')
            parts.append(
                f'<div class="finding-header">'
                f'<span class="{sev_class}">{icon} {escape(sev.upper())}</span>'
                f'<span class="confidence {conf_class}">'
                f"confidence: {escape(conf)}</span>"
                f"</div>"
            )
            parts.append(f'<div class="description">{escape(finding.description)}</div>')

            if finding.affected_sids:
                sids_str = ", ".join(f"SID {s}" for s in finding.affected_sids)
                parts.append(f'<div class="detail">Affected rules: {escape(sids_str)}</div>')

            if finding.recommendation:
                parts.append(
                    f'<div class="detail">Recommendation: {escape(finding.recommendation)}</div>'
                )

            if finding.caveat:
                parts.append(f'<div class="caveat">\u2139\ufe0f {escape(finding.caveat)}</div>')

            if finding.reference:
                parts.append(f'<div class="reference">Ref: {escape(finding.reference)}</div>')

            parts.append("</div>")  # .finding

    if not any_findings:
        parts.append("<p>No findings to display.</p>")

    # Footer
    parts.append(
        f'<div class="footer">Generated by Suricata Rule Generator &mdash; '
        f"AI Analysis &middot; {escape(timestamp_display)}</div>"
    )

    parts.append("</body>")
    parts.append("</html>")

    return "\n".join(parts)


